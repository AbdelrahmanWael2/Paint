package com.Paint.Paint.Shapes;


public class Rectangle  {
    public Rectangle(){}
    private int x = 70;
    private int y = 50;
    private int xP = 0;
    private int yP = 0;
    private String Name = "rectangle";


    public int getX()
    {
        return this.x;
    }
    public int getY()
    {
        return this.y;
    }
    public int getxP()
    {
        return this.xP;
    }
    public int getyP()
    {
        return this.yP;
    }
    public String getName()
    {
        return this.Name;
    }
 }
 
